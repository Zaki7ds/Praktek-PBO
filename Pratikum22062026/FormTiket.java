    private void btnHargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHargaActionPerformed
        int kelas = (int)cbKelas.getSelectedIndex();
        
        if (!rbBkt.isSelected()&&!rbSlk.isSelected()&&!rbPar.isSelected()){
            JOptionPane.showMessageDialog(this,"Tujuan Belum Dipilih","Pesan Kesalahan",JOptionPane.ERROR_MESSAGE );
        }else if(cbKelas.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this,"Tujuan Belum Dipilih","Pesan Kesalahan",JOptionPane.ERROR_MESSAGE );
        }
        switch(kelas){
            case 1 :
                if(rbBkt.isSelected())
                    txtHarga.setText("25000");
                else if(rbSlk.isSelected())
                    txtHarga.setText("20000");
                else
                    txtHarga.setText("15000");
            break;
            case 2 :
                if(rbBkt.isSelected())
                    txtHarga.setText("50000");
                else if(rbSlk.isSelected())
                    txtHarga.setText("40000");
                else
                    txtHarga.setText("30000");
            
            case 3 :
                if(rbBkt.isSelected())
                    txtHarga.setText("80000");
                else if(rbSlk.isSelected())
                    txtHarga.setText("70000");    
                else
                    txtHarga.setText("55000");
        }
    }//GEN-LAST:event_btnHargaActionPerformed

