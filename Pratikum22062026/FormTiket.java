    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        try{
        int jumlah = Integer.parseInt(txtJumlah.getText());
        if(jumlah<0){
            JOptionPane.showMessageDialog(this,"Jumlah tidak falid","Error",JOptionPane.ERROR_MESSAGE);
        }else{
        double harga = Double.parseDouble(txtHarga.getText());
        double total = jumlah * harga;
        double diskon = 0;
        if(jumlah>5)
            diskon = 0.1*total;
        double bayar = total - diskon;
        
        txtTot.setText(Double.toString(total));
        txtDiskon.setText(Double.toString(diskon ));
        txtTotal.setText(Double.toString(bayar));
        }
        }catch(NumberFormatException e){
               JOptionPane.showMessageDialog(this,"Jumlah harus bilangan bulat","Pesan ERROR",JOptionPane.ERROR_MESSAGE);
               txtJumlah.setText("");
               txtJumlah.requestFocus();
               } 
    }//GEN-LAST:event_btnHitungActionPerformed

